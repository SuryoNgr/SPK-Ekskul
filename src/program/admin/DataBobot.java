    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed

    if (!txt_b1.isEnabled() && !txt_b2.isEnabled() && !txt_b3.isEnabled() && !txt_b4.isEnabled()) {
        JOptionPane.showMessageDialog(null, "Bobot sudah terisi dan maksimal 1. Tidak dapat menyimpan data baru.");
        return; 
    }

    String sql = "INSERT INTO tbl_bobot (b1, b2, b3, b4) VALUES (?, ?, ?, ?)";
    try {
        PreparedStatement stat = conn.getconnetion().prepareStatement(sql);
        stat.setString(1, txt_b1.getText());
        stat.setString(2, txt_b2.getText());
        stat.setString(3, txt_b3.getText());
        stat.setString(4, txt_b4.getText());
        stat.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        clear();
        tampilBobot();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data gagal disimpan " + e);
    }
    }//GEN-LAST:event_btn_simpanActionPerformed

