    private void tbl_bobotMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_bobotMouseClicked
  
    int row = tbl_bobot.getSelectedRow();

    
    if (row >= 0) {
        // Ambil nilai dari tabel dan set ke JTextField
        txt_b1.setText(tbl_bobot.getValueAt(row, 0).toString());
        txt_b2.setText(tbl_bobot.getValueAt(row, 1).toString());
        txt_b3.setText(tbl_bobot.getValueAt(row, 2).toString());
        txt_b4.setText(tbl_bobot.getValueAt(row, 3).toString());
        
        // Nonaktifkan JTextField karena data sudah ada
        txt_b1.setEnabled(false);
        txt_b2.setEnabled(false);
        txt_b3.setEnabled(false);
        txt_b4.setEnabled(false);
    }
    }//GEN-LAST:event_tbl_bobotMouseClicked

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
       
     try {
        
        String sqlDelete = "DELETE FROM tbl_bobot";
        PreparedStatement statDelete = conn.getconnetion().prepareStatement(sqlDelete);
        statDelete.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus");

        clear();
         normal();
        DefaultTableModel model = (DefaultTableModel) tbl_bobot.getModel();
        model.setRowCount(0); 
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Data gagal dihapus " + ex);
    }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void clear(){
        txt_b1.setText("");
        txt_b2.setText((""));
        txt_b3.setText("");
        txt_b4.setText("");
    }
    
    private void normal(){
    txt_b1.setEnabled(true);
    txt_b2.setEnabled(true);
    txt_b3.setEnabled(true);
    txt_b4.setEnabled(true);
    }
    
    public void tampilBobot() {
    tblBobot = new DefaultTableModel();
    tblBobot.addColumn("Minat");
    tblBobot.addColumn("Bakat");
    tblBobot.addColumn("Prestasi");
    tblBobot.addColumn("Dukungan Orang Tua");
    tbl_bobot.setModel(tblBobot);
    
    try {
        ResultSet res = conn.ambilData("SELECT * FROM tbl_bobot");
        if (res.next()) {
            
            txt_b1.setEnabled(false);
            txt_b2.setEnabled(false);
            txt_b3.setEnabled(false);
            txt_b4.setEnabled(false);
            
            tblBobot.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4)});
        } else {
            txt_b1.setEnabled(true);
            txt_b2.setEnabled(true);
            txt_b3.setEnabled(true);
            txt_b4.setEnabled(true);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, ex);
    }
}

    
